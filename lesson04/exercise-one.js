function square() {
	let input = prompt('Enter a number');
	let result = 0;
	result = Number(input) ** 2;
	alert(result);
}

function multiply() {
	let input1 = prompt('Enter 1st number');
	let input2 = prompt('Enter 2nd number');
	let result = 0;
	result = Number(input1) * Number(input2);
	alert(result);
}